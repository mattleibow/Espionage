namespace Espionage
{
	/// <summary>
	/// Represents the current location status of a top-secret document.
	/// </summary>
	public enum DocumentLocation
	{
		/// <summary>
		/// At the enemy's facility.
		/// </summary>
		AtEnemyFacility,

		/// <summary>
		/// On the secret agent's person.
		/// </summary>
		OnAgent,

		/// <summary>
		/// On the intelligence operative's person.
		/// </summary>
		OnIntelligenceOperative,

		/// <summary>
		/// At friendly headquarters.
		/// </summary>
		AtHeadquarters
	}
}
